/**
 * @description: rle算法解密
 * @detail:
 * @param {unsigned char} *inbuf 输入buf
 * @param {int} inSize 输入大小
 * @param {unsigned char} *outbuf 输出buf
 * @param {int} onuBufSize 最大输出buf大小
 * @param {int} *outsize 解析inbuf数
 * @return {*}
 * @author: lkc
 */
int RLE_Decode(const unsigned char *inbuf, int inSize, unsigned char *outbuf, int onuBufSize, int *outsize)
{
    const unsigned char *src = inbuf;
    int i;
    int decSize = 0;
    int inbuf_out = 0;

    while (src < (inbuf + inSize)) {
        unsigned char sign = *src++; // sign = 0x00
        inbuf_out++;
        int count = sign & 0x7F; // count = 0x00
        if ((decSize + count) > onuBufSize) {
            return -1;
        }
        if ((sign & 0x80) == 0x80) { //
            for (i = 0; i < count; i++) {
                outbuf[decSize++] = *src;
            }
            src++;
            inbuf_out++;
        } else {
            for (i = 0; i < count; i++) { // 00
                outbuf[decSize++] = *src++;
                inbuf_out++;
            }
        }
    }
    *outsize = inbuf_out;
    return decSize;
}

/**
 * @description: 获取地址数据
 * @detail:
 * @param {uint32_t} read_size
 * @param {uint32_t} need_size
 * @return {*}
 * @author: lkc
 */
#define MAX_RLE_BUF 1024
int RLE_GetAddrData(uint32_t read_size, uint32_t need_size, unsigned char *out)
{
    int flag = 0;

    int length = sizeof(encode_iso);
    int inbuf_out;
    int total_size = 0;
    int get_size = 0;
    int has_get_size = 0;
    unsigned char outbuf[MAX_RLE_BUF] = {0};

    for (size_t i = 0; i < length;) {
        // 解析数据2 因为一组数据最少2个,这样方便解析
        int tmp = RLE_Decode(&encode_iso[i], 2, outbuf, MAX_RLE_BUF, &inbuf_out);
        if (tmp == -1) {
            return -2;
        }
        i += inbuf_out;
        total_size += tmp;

        if (total_size > read_size) {
            get_size = total_size - read_size;
            if (0 == flag) {
                flag = 1;
                memcpy(out, outbuf + tmp - get_size, get_size);
            } else {
                memcpy(out + has_get_size, outbuf, tmp);
            }
            if (get_size >= need_size) {
                break;
            }
            has_get_size = get_size;
        }
    }

    return 0;
}
